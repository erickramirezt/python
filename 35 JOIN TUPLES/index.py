# PYTHON - JOIN TUPLES

# JOIN TWO TUPLES
# TO JOIN TWO OR MORE TUPLES YOU CAN USE THE + OPERATOR
# EXAMPLE
# JOIN TWO TUPLES
tuple1 = ("a", "b", "c")
tuple2 = (1, 2, 3)
tuple3 = tuple1 + tuple2
print(tuple3)

# MULTIPLY TUPLES
# IF YOU WANT TO MULTIPLY THE CONTENTS OF A TUPLE A GIVEN NUMBER OF TIMES, YOU CAN USE 
# THE * OPERATOR
# EXAMPLE
# MULTIPLY THE FRUITS TUPLE BY 2
fruits = ("apple", "banana", "cherry")
mytuple = fruits * 2
print(mytuple)